class LacoRepeticao{
	public static void main(String[] args){
		
		for(int i = 0; i < 10; i++){
			System.out.println(i + " ola");
		}
		int x = 0;
		while(x < 10){
			System.out.println("Hello");
			x++;
		}

		int j = 0;
		do{
			System.out.println(j + " j^2 = " + j * j);
			j++;
		}while(j < 10);
	}
}